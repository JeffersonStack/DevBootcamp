// && - AND
let x = 3;
let y = 7;

// Testes
if (x === 3 && y === 7) {
  console.log("Correto!!");
} else {
  console.log("Errado!");
}

// || - OR
if (x === 3 || y === 3) {
  console.log("Correto!!");
}

// ! - NOT
if (x !== 3) {
  console.log("Correto!");
} else {
  console.log("Errado!!");
}
