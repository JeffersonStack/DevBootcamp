// OPERADORES DE IGUALDADE / RELACIONAIS
// igual, diferente(== , !=)

let x = 0;
let y = 1;
let nome = "Jefferson";

// testes
console.log(x == 1);
console.log(y == 1);
console.log(x != 1);
console.log(typeof nome);

// OPERADORES RELACIONAIS
// (<, >, <=, >=)

let a = 20;
let b = 30;

// testes
console.log(a < b); // true
console.log(a > b); // false
console.log(a >= 20); // true
console.log(b <= 20); // false
